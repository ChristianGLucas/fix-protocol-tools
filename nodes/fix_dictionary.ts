// Y/N boolean-flag tags — decoded as "Yes"/"No" by DecodeEnumValue.
export const BOOLEAN_TAGS = new Set([
  43, 97, 114, 121, 123, 141, 208, 266, 377,
]);

/** Decodes a raw enum/boolean value for `tag`; returns null if not found. */
export function decodeEnumValue(tag: number, value: string): string | null {
  if (BOOLEAN_TAGS.has(tag)) {
    if (value === 'Y') return 'Yes';
    if (value === 'N') return 'No';
    return null;
  }
  const table = ENUMS[tag];
  if (!table) return null;
  return Object.prototype.hasOwnProperty.call(table, value) ? table[value] : null;
}
